# 20%几率有视频
HAS_VIDEO = ["https://www.bilibili.com/video/BV1GJ411x7jY"]

def gen_sql():
    lines = ["-- V5: 海量种子数据（自动生成）", "BEGIN;", ""]
    # 新增食材
    lines.append("-- 新增食材")
    for name, unit, cat, cal in NEW_INGREDIENTS:
        lines.append(f"INSERT INTO ingredients (name,unit,category,calories_per_unit) VALUES ('{name}','{unit}','{cat}',{cal}) ON CONFLICT DO NOTHING;")
    lines.append("")

    # 复用V4的辅助函数
    lines.append("CREATE OR REPLACE FUNCTION get_ing_id(n TEXT) RETURNS BIGINT AS $$ SELECT id FROM ingredients WHERE name=n LIMIT 1; $$ LANGUAGE SQL;")
    lines.append("CREATE OR REPLACE FUNCTION get_rid(t TEXT) RETURNS BIGINT AS $$ SELECT id FROM recipes WHERE title=t LIMIT 1; $$ LANGUAGE SQL;")
    lines.append("")

    count = 0
    for name, cuisine, diff, cook, desc, ings in RECIPES:
        video = random.choice(HAS_VIDEO) if random.random() < 0.2 else "NULL"
        video_str = f"'{video}'" if video != "NULL" else "NULL"
        lines.append(f"INSERT INTO recipes(title,description,image_url,video_url,prep_time,cook_time,difficulty,cuisine_type,calories,servings,status) VALUES")
        cal = 200
        lines.append(f"('{name}','{desc}','https://placehold.co/400x300/10b981/white?text={name}',{video_str},{random.randint(5,20)},{cook},'{diff}','{cuisine}',{cal},{random.randint(2,4)},'published');")

        # recipe_ingredients
        for i, ing_name in enumerate(ings):
            ing = None
            for ing_def in INGREDIENTS + NEW_INGREDIENTS:
                if ing_def[0] == ing_name:
                    ing = ing_def
                    break
            qty = random.randint(1, 500)
            unit = ing[1] if ing else "克"
            lines.append(f"INSERT INTO recipe_ingredients(recipe_id,ingredient_id,quantity,unit) VALUES (get_rid('{name}'),get_ing_id('{ing_name}'),{qty},'{unit}');")

        # recipe_steps (3-5 steps)
        steps = [
            (1, f"{ings[0] if ings else '食材'}洗净切好备用"),
            (2, "锅中热油，爆香姜蒜"),
            (3, "加入主料翻炒至变色"),
            (4, "加入调味料和适量水煮至入味"),
            (5, "收汁出锅装盘"),
        ]
        random.shuffle(steps)
        for step_num, instr in steps[:random.randint(3,5)]:
            sort_step = step_num if step_num <= 5 else step_num
            lines.append(f"INSERT INTO recipe_steps(recipe_id,step_number,instruction,timer_minutes) VALUES (get_rid('{name}'),{sort_step},'{instr}',{random.randint(1,10)});")

        count += 1
        if count % 20 == 0:
            print(f"  {count}/{len(RECIPES)} recipes generated...")

    lines.append("")
    lines.append("DROP FUNCTION IF EXISTS get_ing_id;")
    lines.append("DROP FUNCTION IF EXISTS get_rid;")
    lines.append("COMMIT;")

    return "\n".join(lines)

if __name__ == "__main__":
    sql = gen_sql()
    with open("V5__massive_seed_data.sql", "w", encoding="utf-8") as f:
        f.write(sql)
    print(f"Generated {len(RECIPES)} recipes into V5__massive_seed_data.sql")
